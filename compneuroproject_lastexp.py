# %% CELL 9 — selectivity index (FIX A) ---------------------------------------
# Standard Cohen's-d / d-prime: difference of group means divided by pooled
# WITHIN-group SD on the RAW activations. The earlier version z-scored across
# all images first, which absorbs the between-group difference into the scale
# and biases d' toward zero.
def selectivity(acts, labels):
    a = acts[labels == "animate"]
    i = acts[labels == "inanimate"]
    pooled_sd = np.sqrt(0.5 * (a.var(0) + i.var(0)) + 1e-8)
    return (a.mean(0) - i.mean(0)) / pooled_sd

