class PreppedDS(Dataset):
    def __init__(self, paths, prep):
        self.paths, self.prep = paths, prep
    def __len__(self): return len(self.paths)
    def __getitem__(self, i):
        return self.prep(Image.open(self.paths[i]).convert("RGB"))

MODEL_REGISTRY = {}     # name -> {'feat': fn, 'prep': transform, 'spatial': fn}

def extract_activations_fast(name, builder, batch_size=BATCH, num_workers=NUM_WORKERS):
    cache = ACT_DIR / f"{name.replace('/','_')}.npz"
    if cache.exists():
        arr = np.load(cache, allow_pickle=False)["acts"]
        if arr.shape[0] == len(meta):
            print(f"[{name}] cached {arr.shape}")
            # still need prep/spatial for later cells
            feat_fn, prep, spatial_fn = builder()
            MODEL_REGISTRY[name] = dict(feat=feat_fn, prep=prep, spatial=spatial_fn)
            return arr
        print(f"[{name}] stale cache {arr.shape}; re-extracting")
        cache.unlink()

    feat_fn, prep, spatial_fn = builder()
    MODEL_REGISTRY[name] = dict(feat=feat_fn, prep=prep, spatial=spatial_fn)

    ds = PreppedDS(meta["path"].tolist(), prep)
    loader = DataLoader(ds, batch_size=batch_size, num_workers=num_workers,
                        shuffle=False, pin_memory=True)

    all_acts = []
    for x in tqdm(loader, desc=f"extract {name}"):
        x = x.to(DEVICE, non_blocking=True)
        with torch.no_grad(), torch.cuda.amp.autocast():
            a = feat_fn(x).float().cpu().numpy()
        all_acts.append(a)
    acts = np.concatenate(all_acts, axis=0)
    np.savez_compressed(cache, acts=acts)
    print(f"[{name}] saved {acts.shape}")
    torch.cuda.empty_cache(); gc.collect()
    return acts

