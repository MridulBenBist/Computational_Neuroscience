# %% CELL 13 — FIGURE 4: memorability modulation (FIX D: vectorised CIs) -----
def _spearman_boot_vec(x, y, n_boot=1000, seed=0):
    """
    Bootstrap CI for Spearman ρ — vectorised.
    Trick: rank the original arrays once, then Pearson-correlate bootstrapped
    rank pairs (Pearson on ranks == Spearman). All bootstrap samples are
    computed in NumPy without a per-iteration scipy call.
    """
    rng = np.random.default_rng(seed)
    rx = rankdata(x); ry = rankdata(y)
    N = len(rx)
    idx = rng.integers(0, N, size=(n_boot, N))
    Rx = rx[idx]; Ry = ry[idx]
    Rxc = Rx - Rx.mean(axis=1, keepdims=True)
    Ryc = Ry - Ry.mean(axis=1, keepdims=True)
    num = (Rxc * Ryc).sum(axis=1)
    den = np.sqrt((Rxc ** 2).sum(axis=1) * (Ryc ** 2).sum(axis=1)) + 1e-12
    rhos = num / den
    return np.percentile(rhos, 2.5), np.percentile(rhos, 97.5)

def within_memorability_corr(acts, sel_vec, mem, mask, K_list=(1, 5, 20), n_boot=1000):
    top_order = np.argsort(sel_vec)[::-1]
    A = acts[mask]; M = mem[mask]
    out = {}
    for K in K_list:
        units = top_order[:K]
        score = A[:, units].mean(axis=1)
        r_point, _ = spearmanr(score, M)
        lo, hi = _spearman_boot_vec(score, M, n_boot=n_boot)
        out[K] = (r_point, lo, hi)
    return out

