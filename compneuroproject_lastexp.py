import timm, open_clip

def build_resnet50():
    m = timm.create_model("resnet50", pretrained=True, num_classes=0, global_pool="avg")
    m.eval().to(DEVICE)
    cfg  = timm.data.resolve_data_config({}, model=m)
    prep = timm.data.create_transform(**cfg)
    feats = {}
    def hook(_, __, out): feats["s"] = out.detach()
    m.layer4.register_forward_hook(hook)
    def feat(x):
        with torch.no_grad(): return m(x)
    def spatial(x):
        with torch.no_grad(): m(x); return feats["s"]
    return feat, prep, spatial

def build_clip_vitb16(head="cls"):
    """
    CLIP ViT-B/16 with selectable feature head.
      head='cls'        -> CLS token (what CLIP was trained on; the canonical choice)
      head='patch_mean' -> mean over patch tokens (matches ResNet avg-pool symmetry)
    """
    assert head in ("cls", "patch_mean")
    model, _, prep = open_clip.create_model_and_transforms(
        "ViT-B-16-quickgelu", pretrained="openai"
    )
    visual = model.visual.eval().to(DEVICE)

    hooked = {}
    def hook(_, __, o):
        hooked["tok"] = o.detach()
    visual.transformer.resblocks[-1].register_forward_hook(hook)

    def _to_NLD(tok, B):
        if tok.shape[0] == B: return tok
        if tok.shape[1] == B: return tok.transpose(0, 1)
        raise RuntimeError(f"unexpected CLIP hook shape {tok.shape} for B={B}")

    def feat(x):
        B = x.shape[0]
        with torch.no_grad():
            _ = visual(x)
            tok = _to_NLD(hooked["tok"], B).contiguous()
            if head == "cls":
                return tok[:, 0, :]              # CLS
            else:
                return tok[:, 1:, :].mean(dim=1) # patch mean

    def spatial(x):
        B = x.shape[0]
        with torch.no_grad():
            _ = visual(x)
            tok = _to_NLD(hooked["tok"], B).contiguous()
            patches = tok[:, 1:, :]
            H = W = int(patches.shape[1] ** 0.5)
            return patches.transpose(1, 2).reshape(B, -1, H, W)

    return feat, prep, spatial

